import Foundation
import SwiftUI

public extension DesignKit {
    enum Font {}
}

public extension DesignKit.Font {
    /// 28 pt, Regular
    static let heading = Font.title
    /// 17 pt, Semibold
    static let title = Font.headline
    /// 15 pt, Regular
    static let subtitle = Font.subheadline
}
